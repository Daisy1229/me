#for loops
my_list = [34,10,55,11]
for value in my_list:
    print(value)

#range
for idx in range(5):
    print(idx)

for idx in range(len(my_list)):
    print(my_list[idx])


#while loops
x = 10
y = 10
while y >= x:
    print(my_list[0])

#while loops
idx = 0
while idx < len(my_list):
    print(my_list[idx])
    idx = idx + 1